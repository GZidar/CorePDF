namespace CorePDF.Contents
{
    /// <summary>
    /// Specifies the alignment of the text in textbox content.
    /// </summary>
    public enum Alignment
    {
        /// <summary>
        /// Position of the textbox is the lower left corner of the first line
        /// </summary>
        Left,
        /// <summary>
        /// Position of the textbox is the mid-point of the first line
        /// </summary>
        Center,
        /// <summary>
        /// Position of the textbox is the lower right corner of the first line
        /// </summary>
        Right 
    }
}